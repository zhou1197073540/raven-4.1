package com.riddler.usr.bean;

public class UserAssetsPoints {
    private String code;
    private String total_sum_points;
    private String date;
    private String add_up;

    public String getTotal_sum_points() {
        return total_sum_points;
    }

    public void setTotal_sum_points(String total_sum_points) {
        this.total_sum_points = total_sum_points;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getAdd_up() {
        return add_up;
    }

    public void setAdd_up(String add_up) {
        this.add_up = add_up;
    }
}
